-- ##################### Update Loop ########################

local preview_t = 0                 

mod.update = function(dt)
    local t = Managers.time and Managers.time:time("main") or 0
    local current_color, is_rgb_active = get_inventory_color(t)
    local locally_wielded = get_locally_wielded_units()

    -- Always advance preview time and fetch UI color for smooth menu animations
    preview_t = preview_t + dt
    local ui_color, ui_is_rgb = get_inventory_color(preview_t)

    -- Build VE whitelist for this frame to prevent setting cross-contamination
    table.clear(_ve_unit_cache)
    local ve_mod = get_mod("visible_equipment")
    if ve_mod then
        local pt = ve_mod.pt and ve_mod:pt()
        if pt and pt.item_units_by_equipment_component then
            for eq_comp, slots in pairs(pt.item_units_by_equipment_component) do
                for slot_name, slot_unit in pairs(slots) do
                    if slot_unit then
                        _ve_unit_cache[slot_unit] = true
                    end
                    local attachments = pt.attachment_units_by_equipment_component[eq_comp]
                    if attachments and attachments[slot_name] and attachments[slot_name][slot_unit] then
                        for _, att in ipairs(attachments[slot_name][slot_unit]) do
                            if att then
                                _ve_unit_cache[att] = true
                            end
                        end
                    end
                end
            end
        end
    end

    -- 1. Update Preview Dummies (Inventory, Cutscenes, Lobbies)
    if #transonic_preview_units > 0 then
        for i = #transonic_preview_units, 1, -1 do
            local unit = transonic_preview_units[i]
            if unit and Unit.alive(unit) then
                local owner = get_weapon_owner_type(unit)
                
                if locally_wielded[unit] then
                    table.remove(transonic_preview_units, i)
                elseif _ve_unit_cache[unit] then
                    -- DO NOTHING. Let Section 2 handle Visible Equipment items to decouple settings!
                elseif ui_color then
                    local should_glow = false
                    
                    if owner == "local" then
                        -- Definitively link "Show Glow Inventory" setting to the local UI mannequin
                        if is_in_inventory_view() then
                            should_glow = settings_cache.inventory_enabled
                        else
                            should_glow = true
                        end
                    elseif owner == "ui" then
                        should_glow = settings_cache.inventory_enabled
                    elseif owner == "teammate" then
                        should_glow = settings_cache.apply_others
                    elseif owner == "unknown" then
                        should_glow = settings_cache.apply_others 
                    end

                    if should_glow then
                        apply_color_to_ve_unit(unit, ui_color, ui_is_rgb)
                    end
                end
            else
                table.remove(transonic_preview_units, i)
            end
        end
    end

    -- 2. Visible Equipment Compatibility
    if settings_cache.ve_glow_enabled and ve_mod then
        local pt = ve_mod.pt and ve_mod:pt()
        if pt and pt.item_units_by_equipment_component then
            for eq_comp, slots in pairs(pt.item_units_by_equipment_component) do
                for slot_name, slot_unit in pairs(slots) do
                    
                    if slot_unit and Unit.alive(slot_unit) and is_transonic_unit[slot_unit] then
                        local owner = get_weapon_owner_type(slot_unit)
                        local should_glow = false

                        if owner == "local" or owner == "ui" then
                            should_glow = true
                        elseif owner == "teammate" then
                             should_glow = settings_cache.apply_others
                        elseif owner == "unknown" then
                            should_glow = settings_cache.apply_others
                        end
                        
                        if should_glow then
                            local c = (owner == "ui") and ui_color or current_color
                            local is_rgb = (owner == "ui") and ui_is_rgb or is_rgb_active

                            if not locally_wielded[slot_unit] then 
                                apply_color_to_ve_unit(slot_unit, c, is_rgb) 
                            end
                            
                            local attachments = pt.attachment_units_by_equipment_component[eq_comp]
                            if attachments and attachments[slot_name] and attachments[slot_name][slot_unit] then
                                for _, att in ipairs(attachments[slot_name][slot_unit]) do
                                    if is_transonic_unit[att] and not locally_wielded[att] then 
                                        apply_color_to_ve_unit(att, c, is_rgb) 
                                    end
                                end
                            end
                        end
                    end
                    
                end
            end
        end
    end

    -- 3. Chordclaw Ability Dynamic Sync
    local cc_mode = settings_cache.chordclaw_mode
    if cc_mode ~= "disabled" then
        local current_player_unit = get_safe_local_player_unit()

        if current_player_unit ~= mod._cached_player_unit then
            mod._cached_player_unit = current_player_unit
            for k in pairs(owner_type_cache) do owner_type_cache[k] = nil end
            for k in pairs(effect_owner_cache) do effect_owner_cache[k] = nil end
            if current_player_unit then
                mod._cached_visual_ext = ScriptUnit.has_extension(current_player_unit, "visual_loadout_system") and ScriptUnit.extension(current_player_unit, "visual_loadout_system")
            else
                mod._cached_visual_ext = nil
            end
            mod._force_cc_update = true
        end

        mod._cc_refresh_counter = mod._cc_refresh_counter + 1
        local periodic_refresh = (mod._cc_refresh_counter % 30 == 0)

        if mod._cached_visual_ext and mod._cached_visual_ext._equipment then
            local claw_slot = mod._cached_visual_ext._equipment["slot_combat_ability"]
            if claw_slot then
                local cc_color, cc_is_rgb = get_chordclaw_color(t, current_color, is_rgb_active)
                local color_key = cc_color and (not cc_is_rgb and string.format("%.3f,%.3f,%.3f", cc_color.x, cc_color.y, cc_color.z) or "rgb_active") or "none"

                if cc_is_rgb or mod._force_cc_update or periodic_refresh or mod._last_cc_color_key ~= color_key or (cc_mode == "both" and mod._last_cc_stance ~= mod._last_known_special_active) then

                    mod._last_cc_stance = mod._last_known_special_active
                    mod._last_cc_color_key = color_key

                    if cc_color then
                        local any_applied = false
                        local function apply_to_unit(u)
                            if u and Unit.alive(u) then
                                for j = 1, #CLAW_MATERIAL_CANDIDATES do
                                    pcall(Unit.set_vector3_for_materials, u, CLAW_MATERIAL_CANDIDATES[j], cc_color, true)
                                end
                                any_applied = true
                            end
                        end
                        apply_to_unit(claw_slot.unit_1p)
                        apply_to_unit(claw_slot.unit_3p)
                        if any_applied then mod._force_cc_update = false end
                    else
                        mod._force_cc_update = false
                    end
                end
            end
        end
    end
end

mod.on_setting_changed = function(setting_id)
    update_settings_cache()
    
    for k in pairs(ve_state_cache) do
        ve_state_cache[k] = nil
    end
    
    mod._force_cc_update = true
    for effect_instance, _ in pairs(tracked_weapon_effects) do
        update_blade_color(effect_instance, 0, true)
    end
    
    if setting_id == "ve_glow_enabled" and settings_cache.ve_glow_enabled then
        if not get_mod("visible_equipment") then
            mod:echo(mod:localize("ve_not_installed_warning"))
        end
    end
end