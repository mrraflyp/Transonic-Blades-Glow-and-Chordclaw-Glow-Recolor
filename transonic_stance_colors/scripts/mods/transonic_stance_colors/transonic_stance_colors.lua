local function get_weapon_owner_type(weapon_unit)
    if not weapon_unit then return "unknown" end
    if owner_type_cache[weapon_unit] then return owner_type_cache[weapon_unit] end
    
    -- ── Step 1: Exact Gear ID Match (Instantly solves cutscene & puppet ownership)
    local item = unit_to_item_map[weapon_unit]
    if item then
        local gear_id = item.gear_id or item.__gear_id
        if gear_id then
            local is_mine = false
            pcall(function()
                local lp = Managers.player:local_player(1)
                if lp then
                    local profile = lp:profile()
                    if profile and profile.loadout then
                        for _, slot_item in pairs(profile.loadout) do
                            if type(slot_item) == "table" then
                                local s_id = slot_item.gear_id or slot_item.__gear_id
                                if s_id == gear_id then
                                    is_mine = true
                                    break
                                end
                            end
                        end
                    end
                end
            end)
            if is_mine then
                owner_type_cache[weapon_unit] = "local"
                return "local"
            end
        end
    end

    -- ── Step 2: Parent Unit / Link Unit Checks
    local link_unit = unit_to_player_map[weapon_unit]
    if not link_unit then 
        owner_type_cache[weapon_unit] = "ui"
        return "ui" 
    end

    local my_account_id = nil
    local my_unique_id = nil
    pcall(function()
        if Managers.player then
            local lp = Managers.player:local_player(1)
            if lp then 
                my_account_id = lp:account_id() 
                my_unique_id = lp:unique_id()
            end
        end
    end)

    -- 1. Gameplay / Hub Player Unit Match
    if Managers.player then
        local ok_p, players = pcall(Managers.player.players, Managers.player)
        if ok_p and players then
            for _, p in pairs(players) do
                if p.player_unit == link_unit then
                    if my_account_id and p:account_id() == my_account_id then
                        owner_type_cache[weapon_unit] = "local"
                        return "local"
                    else
                        owner_type_cache[weapon_unit] = "teammate"
                        return "teammate"
                    end
                end
            end
        end
    end

    -- 2. Cutscene Puppet Match
    if ScriptUnit.has_extension(link_unit, "cutscene_character_system") then
        local cutscene_ext = ScriptUnit.extension(link_unit, "cutscene_character_system")
        local unique_id = cutscene_ext._player_unique_id
        if unique_id and my_unique_id then
            if unique_id == my_unique_id then
                owner_type_cache[weapon_unit] = "local"
                return "local"
            else
                owner_type_cache[weapon_unit] = "teammate"
                return "teammate"
            end
        end
    end

    -- 3. VE Profile Match
    local ok_prof, profile = pcall(Unit.get_data, link_unit, "visible_equipment_profile")
    if ok_prof and profile and profile.account_id and my_account_id then
        if profile.account_id == my_account_id then
            owner_type_cache[weapon_unit] = "local"
            return "local"
        else
            owner_type_cache[weapon_unit] = "teammate"
            return "teammate"
        end
    end

    -- 4. Menu & Lobby Spawners
    local function check_view_spawners(view_name)
        local ok, view = pcall(function() return Managers.ui and Managers.ui:view_active(view_name) and Managers.ui:view_instance(view_name) end)
        if ok and view and type(view._spawn_slots) == "table" then
            for _, slot in pairs(view._spawn_slots) do
                if slot.occupied and slot.profile_spawner then
                    local ok_u, u = pcall(slot.profile_spawner.spawned_character_unit, slot.profile_spawner)
                    if ok_u and u == link_unit then
                        local acc_id
                        pcall(function()
                            acc_id = slot.account_id or (slot.player and slot.player:account_id()) or (slot.player_info and slot.player_info:account_id())
                        end)
                        if my_account_id and acc_id == my_account_id then
                            return "local"
                        elseif acc_id then
                            return "teammate"
                        end
                    end
                end
            end
        end
        return nil
    end

    local views_to_check = {
        "end_view", "expedition_view", "lobby_view", 
        "havoc_background_view", "havoc_play_view", 
        "mission_intro_view", "cutscene_view"
    }
    for _, v_name in ipairs(views_to_check) do
        local res = check_view_spawners(v_name)
        if res then
            owner_type_cache[weapon_unit] = res
            return res
        end
    end

    if is_in_inventory_view() then
        return "local"
    end

    return "unknown"
end

local function is_effect_local_player(effect_instance)
    if not effect_instance then return false end
    if effect_owner_cache[effect_instance] ~= nil then return effect_owner_cache[effect_instance] end

    local is_local = false
    local current_player_unit = get_safe_local_player_unit()

    if current_player_unit and ScriptUnit.has_extension(current_player_unit, "visual_loadout_system") then
        local visual_ext = ScriptUnit.extension(current_player_unit, "visual_loadout_system")
        if visual_ext._wieldable_slot_scripts then
            for _, scripts in pairs(visual_ext._wieldable_slot_scripts) do
                for _, script in ipairs(scripts) do
                    if script == effect_instance then
                        is_local = true
                        break
                    end
                end
                if is_local then break end
            end
        end
    end

    -- Walk every player's wieldable_slot_scripts to find who owns this effect.
    if not is_local and Managers.player then
        local ok_lp, lp = pcall(Managers.player.local_player, Managers.player, 1)
        local ok_p,  players = pcall(Managers.player.players,      Managers.player)
        if ok_lp and lp and ok_p and players then
            for _, p in pairs(players) do
                local pu = p.player_unit
                if pu and ScriptUnit.has_extension(pu, "visual_loadout_system") then
                    local ve = ScriptUnit.extension(pu, "visual_loadout_system")
                    if ve._wieldable_slot_scripts then
                        local found = false
                        for _, scripts in pairs(ve._wieldable_slot_scripts) do
                            for _, script in ipairs(scripts) do
                                if script == effect_instance then found = true; break end
                            end
                            if found then break end
                        end
                        if found then
                            if p == lp then is_local = true end
                            break
                        end
                    end
                end
            end
        end
    end

    if not is_local then
        local weapon_unit = nil
        if effect_instance._weapon_material_variables_3p and effect_instance._weapon_material_variables_3p[1] then
            weapon_unit = effect_instance._weapon_material_variables_3p[1].unit
        elseif effect_instance._weapon_material_variables_1p and effect_instance._weapon_material_variables_1p[1] then
            weapon_unit = effect_instance._weapon_material_variables_1p[1].unit
        end

        if weapon_unit then
            local owner = get_weapon_owner_type(weapon_unit)
            if owner == "local" then
                is_local = true
            elseif owner == "unknown" then
                return false 
            end
        end
    end

    if is_local then effect_owner_cache[effect_instance] = true end
    return is_local
end

